import pytest
from ConvexHull.ConvexHull import ConvexHull
from Point.Point import Point


def test_convexhull_rectangle():
    points = [Point(0, 0), Point(0, 1), Point(1, 1), Point(0.5, 0.5)]
    result = [Point(0, 0), Point(1, 1), Point(0, 1)]
    assert ConvexHull(points).hull == result


def test_convexhull_straight_line():
    points = [Point(0, 0), Point(1, 1), Point(2, 2), Point(3, 3)]
    result = [Point(0, 0), Point(3, 3), Point(3, 3)]
    assert ConvexHull(points).hull == result


